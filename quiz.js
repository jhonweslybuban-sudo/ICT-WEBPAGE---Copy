// Function to start a quiz
function startQuiz(module) {
    const quiz = quizData[module];
    if (!quiz) return;

    // Create modal overlay
    const overlay = document.createElement('div');
    overlay.className = 'quiz-overlay';
    
    // Create quiz container
    const quizContainer = document.createElement('div');
    quizContainer.className = 'quiz-container';
    quizContainer.innerHTML = `
        <div class="quiz-header">
            <h2>${quiz.title}</h2>
            <button onclick="closeQuiz()" class="close-btn">&times;</button>
        </div>
        <div id="quiz-questions"></div>
        <button onclick="submitQuiz('${module}')" class="submit-btn">Submit Quiz</button>
    `;

    // Add questions
    const questionsContainer = quizContainer.querySelector('#quiz-questions');
    quiz.questions.forEach((q, index) => {
        const questionDiv = document.createElement('div');
        questionDiv.className = 'question';
        questionDiv.innerHTML = `
            <p>${index + 1}. ${q.question}</p>
            <div class="options">
                ${q.options.map((opt, i) => `
                    <label>
                        <input type="radio" name="q${index}" value="${i}">
                        ${opt}
                    </label>
                `).join('')}
            </div>
        `;
        questionsContainer.appendChild(questionDiv);
    });

    // Show quiz
    const existingQuiz = document.querySelector('.quiz-overlay');
    if (existingQuiz) {
        existingQuiz.remove();
    }
    
    overlay.appendChild(quizContainer);
    document.body.appendChild(overlay);
}

// Function to close quiz
function closeQuiz() {
    const overlay = document.querySelector('.quiz-overlay');
    if (overlay) {
        overlay.remove();
    }
}

// Function to submit quiz
function submitQuiz(module) {
    const quiz = quizData[module];
    const questions = document.querySelectorAll('.question');
    let score = 0;

    questions.forEach((q, index) => {
        const selected = q.querySelector('input[type="radio"]:checked');
        if (selected && parseInt(selected.value) === quiz.questions[index].correct) {
            score++;
        }
    });

    const percentage = (score / quiz.questions.length) * 100;
    alert(`Your score: ${score}/${quiz.questions.length} (${percentage}%)`);
    closeQuiz();
}

